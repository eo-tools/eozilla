class TestLocalPackages:
    def test_pip_install_command_generated(self, tmp_path, monkeypatch):
        ws = _workspace(tmp_path, packages=["my-pkg"])
        monkeypatch.chdir(ws)
        _, content = _gen(
            tmp_path,
            packages_dir=ws / "packages",
            local_packages=["my-pkg"],
        )
        assert "pip install --no-deps --no-build-isolation" in content
        assert "./packages/my-pkg" in content

    def test_pip_path_uses_workdir_and_pixi_env(self, tmp_path, monkeypatch):
        ws = _workspace(tmp_path, packages=["my-pkg"])
        monkeypatch.chdir(ws)
        _, content = _gen(
            tmp_path,
            workdir="/app",
            pixi_env="prod",
            packages_dir=ws / "packages",
            local_packages=["my-pkg"],
        )
        assert "/app/.pixi/envs/prod/bin/pip" in content

    def test_pip_install_after_pixi_install(self, tmp_path, monkeypatch):
        ws = _workspace(tmp_path, packages=["my-pkg"])
        monkeypatch.chdir(ws)
        _, content = _gen(
            tmp_path,
            packages_dir=ws / "packages",
            local_packages=["my-pkg"],
        )
        assert content.index("pip install") > content.index("pixi install --locked")

    def test_multiple_local_packages(self, tmp_path, monkeypatch):
        ws = _workspace(tmp_path, packages=["pkg-a", "pkg-b"])
        monkeypatch.chdir(ws)
        _, content = _gen(
            tmp_path,
            packages_dir=ws / "packages",
            local_packages=["pkg-a", "pkg-b"],
        )
        assert "./packages/pkg-a" in content
        assert "./packages/pkg-b" in content

    def test_post_install_commands_appended_after_pip(self, tmp_path, monkeypatch):
        ws = _workspace(tmp_path, packages=["my-pkg"])
        monkeypatch.chdir(ws)
        _, content = _gen(
            tmp_path,
            packages_dir=ws / "packages",
            local_packages=["my-pkg"],
            post_install_commands=["RUN echo done"],
        )
        assert content.index("pip install") < content.index("RUN echo done")


