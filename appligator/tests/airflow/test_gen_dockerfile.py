    def test_feature_path_dep_stripped(self, tmp_path):
        src = tmp_path / "pyproject.toml"
        src.write_text(
            "[tool.pixi.feature.local-deps.pypi-dependencies]\n"
            'appligator = { path = "../eozilla/appligator", editable = true }\n'
        )
        dest = tmp_path / "out.toml"
        _copy_pyproject_toml_stripped(src, dest)
        import tomllib

        data = tomllib.loads(dest.read_text())
        feature_deps = (
            data["tool"]["pixi"]["feature"]["local-deps"]["pypi-dependencies"]
        )
        assert "appligator" not in feature_deps
