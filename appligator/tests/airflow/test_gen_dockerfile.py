class TestPackagesDir:
    def test_packages_copied_to_build_context(self, tmp_path, monkeypatch):
        ws = _workspace(tmp_path, packages=["my-pkg"])
        monkeypatch.chdir(ws)
        out = tmp_path / "out"
        generate(
            output_dir=out,
            packages_dir=ws / "packages",
            local_packages=["my-pkg"],
        )
        assert (out / "packages" / "my-pkg").is_dir()

    def test_only_listed_packages_copied(self, tmp_path, monkeypatch):
        ws = _workspace(tmp_path, packages=["pkg-a", "pkg-b", "pkg-c"])
        monkeypatch.chdir(ws)
        out = tmp_path / "out"
        generate(
            output_dir=out,
            packages_dir=ws / "packages",
            local_packages=["pkg-a"],
        )
        assert (out / "packages" / "pkg-a").is_dir()
        assert not (out / "packages" / "pkg-b").exists()
        assert not (out / "packages" / "pkg-c").exists()

    def test_packages_copy_instruction_in_build_stage(self, tmp_path, monkeypatch):
        ws = _workspace(tmp_path, packages=["my-pkg"])
        monkeypatch.chdir(ws)
        _, content = _gen(
            tmp_path,
            packages_dir=ws / "packages",
            local_packages=["my-pkg"],
        )
        build_stage = content[: content.index("FROM debian:bookworm-slim")]
        assert "COPY ./packages ./packages" in build_stage

    def test_packages_not_copied_to_runtime_when_local_packages_set(
        self, tmp_path, monkeypatch
    ):
        ws = _workspace(tmp_path, packages=["my-pkg"])
        monkeypatch.chdir(ws)
        _, content = _gen(
            tmp_path,
            packages_dir=ws / "packages",
            local_packages=["my-pkg"],
        )
        runtime_stage = content[content.index("FROM debian:bookworm-slim") :]
        assert "COPY --from=build" not in runtime_stage or (
            "/packages" not in runtime_stage
        )

    def test_packages_copied_to_runtime_when_no_local_packages(
        self, tmp_path, monkeypatch
    ):
        ws = _workspace(tmp_path, packages=["my-pkg"])
        monkeypatch.chdir(ws)
        _, content = _gen(tmp_path, packages_dir=ws / "packages")
        runtime_stage = content[content.index("FROM debian:bookworm-slim") :]
        assert "COPY --from=build" in runtime_stage
        assert "/packages" in runtime_stage

    def test_custom_packages_dirname_used_in_dockerfile(
        self, tmp_path, monkeypatch
    ):
        ws = tmp_path / "ws"
        ws.mkdir()
        (ws / "pyproject.toml").write_text("[project]\nname = 'test'\n")
        (ws / "pixi.lock").write_text("# lock\n")
        libs = ws / "libs"
        (libs / "my-pkg").mkdir(parents=True)
        (libs / "my-pkg" / "pyproject.toml").write_text("")
        monkeypatch.chdir(ws)
        _, content = _gen(
            tmp_path,
            packages_dir=libs,
            local_packages=["my-pkg"],
        )
        assert "COPY ./libs ./libs" in content


