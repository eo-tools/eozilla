from pathlib import Path

import pytest

from appligator.airflow.gen_dockerfile import (
    _copy_pyproject_toml_stripped,
    generate,
)

def _workspace(tmp_path: Path, *, packages: list[str] | None = None) -> Path:
    """Create a minimal pixi workspace under tmp_path."""
    (tmp_path / "pyproject.toml").write_text("[project]\nname = 'test'\n")
    (tmp_path / "pixi.lock").write_text("# lock\n")
    if packages:
        for pkg in packages:
            pkg_dir = tmp_path / "packages" / pkg
            pkg_dir.mkdir(parents=True)
            (pkg_dir / "pyproject.toml").write_text(
                f"[project]\nname = '{pkg}'\n"
            )
    return tmp_path


def _gen(tmp_path: Path, **kwargs) -> tuple[Path, str]:
    """Call gen_dockerfile from a workspace root and return (dockerfile_path, content)."""
    ws = _workspace(tmp_path, packages=kwargs.pop("_packages", None))
    out = tmp_path / "image"
    import os

    old = os.getcwd()
    os.chdir(ws)
    try:
        df = generate(output_dir=out, **kwargs)
    finally:
        os.chdir(old)
    return df, df.read_text()


class TestValidation:
    def test_unsupported_package_manager_raises(self, tmp_path, monkeypatch):
        monkeypatch.chdir(_workspace(tmp_path))
        with pytest.raises(ValueError, match="Unsupported package manager"):
            generate(package_manager="uv", output_dir=tmp_path / "out")

    def test_local_packages_without_packages_dir_raises(self, tmp_path, monkeypatch):
        monkeypatch.chdir(_workspace(tmp_path))
        with pytest.raises(ValueError, match="local_packages requires packages_dir"):
            generate(
                output_dir=tmp_path / "out",
                local_packages=["my-pkg"],
            )

    def test_missing_pyproject_toml_raises(self, tmp_path, monkeypatch):
        (tmp_path / "pixi.lock").write_text("")
        monkeypatch.chdir(tmp_path)
        with pytest.raises(FileNotFoundError, match="pyproject.toml"):
            generate(output_dir=tmp_path / "out")

    def test_missing_pixi_lock_raises(self, tmp_path, monkeypatch):
        (tmp_path / "pyproject.toml").write_text("")
        monkeypatch.chdir(tmp_path)
        with pytest.raises(FileNotFoundError, match="pixi.lock"):
            generate(output_dir=tmp_path / "out")

    def test_extra_files_missing_source_raises(self, tmp_path, monkeypatch):
        monkeypatch.chdir(_workspace(tmp_path))
        with pytest.raises(FileNotFoundError, match="extra_files entry"):
            generate(
                output_dir=tmp_path / "out",
                extra_files={"data": tmp_path / "nonexistent"},
            )

    def test_local_packages_missing_package_raises(self, tmp_path, monkeypatch):
        ws = _workspace(tmp_path)
        pkgs_dir = ws / "packages"
        pkgs_dir.mkdir()
        monkeypatch.chdir(ws)
        with pytest.raises(FileNotFoundError, match="not found in"):
            generate(
                output_dir=tmp_path / "out",
                packages_dir=pkgs_dir,
                local_packages=["nonexistent-pkg"],
            )


class TestOutputDir:
    def test_returns_dockerfile_path(self, tmp_path, monkeypatch):
        monkeypatch.chdir(_workspace(tmp_path))
        df = generate(output_dir=tmp_path / "out")
